    private void loadReports() {
        try (Connection con = DBConnection.connect()) {

            // ðŸ”¹ Total Users
            String sqlUsers = "SELECT COUNT(*) AS total FROM users";
            PreparedStatement pstUsers = con.prepareStatement(sqlUsers);
            ResultSet rsUsers = pstUsers.executeQuery();
            if (rsUsers.next()) {
                lblTotalUsers.setText("Total Users: " + rsUsers.getInt("total"));
            }

            // ðŸ”¹ Total Anime in Watchlists
            String sqlAnime = "SELECT COUNT(*) AS total FROM watchlist";
            PreparedStatement pstAnime = con.prepareStatement(sqlAnime);
            ResultSet rsAnime = pstAnime.executeQuery();
            if (rsAnime.next()) {
                lblTotalAnime.setText("Total Anime Entries: " + rsAnime.getInt("total"));
            }

            // ðŸ”¹ Average Rating
            String sqlAvg = "SELECT ROUND(AVG(rating), 2) AS avg_rating FROM watchlist";
            PreparedStatement pstAvg = con.prepareStatement(sqlAvg);
            ResultSet rsAvg = pstAvg.executeQuery();
            if (rsAvg.next()) {
                lblAvgRating.setText("Average Rating: " + rsAvg.getDouble("avg_rating"));
            }

            // ðŸ”¹ Top 5 Most Watched Anime
            String sqlTop = """
                SELECT title, COUNT(title) AS watch_count
                FROM watchlist
                GROUP BY title
                ORDER BY watch_count DESC
                LIMIT 5
            """;
            PreparedStatement pstTop = con.prepareStatement(sqlTop);
            ResultSet rsTop = pstTop.executeQuery();

            DefaultTableModel model = (DefaultTableModel) tblTopAnime.getModel();
            model.setRowCount(0);

            while (rsTop.next()) {
                model.addRow(new Object[]{
                    rsTop.getString("title"),
                    rsTop.getInt("watch_count")
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading reports: " + e.getMessage());
        }
    }

