    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String newUsername = txtNewUsername.getText().trim();

        if (newUsername.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a new username!");
            return;
        }

        try (Connection conn = DBConnection.connect()) {
            // ðŸ”¹ Check if username already exists
            String checkUserSql = "SELECT * FROM users WHERE username=?";
            PreparedStatement checkPst = conn.prepareStatement(checkUserSql);
            checkPst.setString(1, newUsername);
            ResultSet rs = checkPst.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Username already exists! Please choose another one.");
                return;
            }
     
            //update username
            String updateSql = "UPDATE users SET username=? WHERE user_id=?";
            PreparedStatement pst = conn.prepareStatement(updateSql);
            pst.setString(1, newUsername);
            pst.setInt(2, userSession.currentUserId);

            int updated = pst.executeUpdate();

            if (updated > 0) {
                JOptionPane.showMessageDialog(this, "Username updated successfully!");
                userSession.currentUser = newUsername; // update current session
                if(parentFrame != null) {
                    parentFrame.updateUsernameLabel(newUsername);
                }
                this.dispose();
                
            } else {
                JOptionPane.showMessageDialog(this, "Error updating username!");
            }
            
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

