    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText();
        String password = txtPassword.getText();

        try (Connection conn = DBConnection.connect()) {
            // ðŸ‘‡ First check if it's an admin
            String adminSql = "SELECT * FROM admin WHERE username=? AND password=?";
            PreparedStatement adminPst = conn.prepareStatement(adminSql);
            adminPst.setString(1, username);
            adminPst.setString(2, password);
            ResultSet adminRs = adminPst.executeQuery();

            if (adminRs.next()) {
                // âœ… Admin login success
                int adminId = adminRs.getInt("admin_id");
                String adminUser = adminRs.getString("username");

                ActivityLogger.log(adminId, adminUser, "Admin logged in");
                AdminDashboard adminDash = new AdminDashboard(adminId, adminUser);
                adminDash.setVisible(true);
                this.dispose();
            } else {
                // ðŸ‘‡ If not admin, check normal users
                String userSql = "SELECT * FROM users WHERE username=? AND password=?";
                PreparedStatement userPst = conn.prepareStatement(userSql);
                userPst.setString(1, username);
                userPst.setString(2, password);
                ResultSet userRs = userPst.executeQuery();

                if (userRs.next()) {
                    // âœ… User login success
                    int userId = userRs.getInt("user_id");
                    String user = userRs.getString("username");
                    
                    userSession.currentUser = userRs.getString("username");
                    userSession.currentUserId = userRs.getInt("user_id");
                    
                    ActivityLogger.log(userId, user, "User logged in");
                    WatchlistDashboard dash = new WatchlistDashboard(userId, user);
                    dash.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Invalid credentials!");
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

