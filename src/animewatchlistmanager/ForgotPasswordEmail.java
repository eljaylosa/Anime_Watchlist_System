    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String userInput = txtEmail.getText().trim();
        String email = null;

    try (Connection conn = DBConnection.connect()) {
        String sql = "SELECT email FROM users WHERE username = ? OR email = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, userInput);
        pst.setString(2, userInput);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            email = rs.getString("email");
        } else {
            JOptionPane.showMessageDialog(this, "No account found with that username or email!");
            return;
        }

        // generate code
        String code = String.valueOf((int)(Math.random() * 900000) + 100000);
        this.verificationCode = code;
        this.userEmail = email;

        // send code
        sendEmail(email, code);

        // show verification fields
        verifyPanel.setVisible(true);
        this.setSize(450, 380);
        //txtCode.setVisible(true);
        //btnVerify.setVisible(true);

        JOptionPane.showMessageDialog(this, "Verification code sent to " + email);

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
    }
    
    }//GEN-LAST:event_jButton1ActionPerformed

